nilai1=int(input("masukkan nilai 1 :"))
nilai2=int(input("masukkan nilai 2 :"))
operasi=input("+-*/%")
if operasi=='+' :
	print(nilai1+nilai2)
elif operasi=='-':
	print(nilai1-nilai2) 
elif operasi=='*' :
	print(nilai1*nilai2)
elif operasi=='/' :
	print(nilai1/nilai2)
elif operasi=='% ':
	print(nilai1%nilai2)
